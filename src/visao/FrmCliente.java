    private void preencherComboEstados() {
        ArrayList nomesEstados = controleCliente.devolverNomesEstados();
        int numeroAtual = 0;
        jComboBoxEstado.removeAllItems();
        do {
            jComboBoxEstado.addItem(nomesEstados.get(numeroAtual));
            numeroAtual = ++numeroAtual;
        } while (nomesEstados.size() > numeroAtual);
    }

    private void preencherComboCidades(int id_Estado) {
        ArrayList nomesCidades = controleCliente.devolverNomesCidades(id_Estado);
        int numeroAtual = 0;
        jComboBoxCidade.removeAllItems();
        do {
            jComboBoxCidade.addItem(nomesCidades.get(numeroAtual));
            numeroAtual = ++numeroAtual;
        } while (nomesCidades.size() > numeroAtual);
    }

    private void preencherComboBairros(int id_Cidade) {
        ArrayList nomesBairros = controleCliente.devolverNomesBairros(id_Cidade);
        int numeroAtual = 0;
        jComboBoxBairro.removeAllItems();
        do {
            jComboBoxBairro.addItem(nomesBairros.get(numeroAtual));
            numeroAtual = ++numeroAtual;
        } while (nomesBairros.size() > numeroAtual);
    }

    private void preencherComboTelefonePorCliente(int id_cliente) {
        ArrayList numerosTelefone = controleTelefone.preencherComboTelefonePorCliente(id_cliente);
        int numeroAtual = 0;
        jComboBoxTelefone.removeAllItems();
        jComboBoxTelefone.setEnabled(true);
        if (!numerosTelefone.isEmpty()) {
            do {
                jComboBoxTelefone.addItem(numerosTelefone.get(numeroAtual));
                numeroAtual = ++numeroAtual;
            } while (numerosTelefone.size() > numeroAtual);
        }
    }

